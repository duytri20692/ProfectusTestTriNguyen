package LikedIn;

public class User {
	String email;
	String pass;
	
	public User() {
		
	}
	public String getEmail() {
		return email;
	}
	
	public void setEmail(String email) {
		this.email = email;
	}
	
	public String getPassword() {
		return pass;
	}
	
	public void setPassword(String password) {
		this.pass = password;
	}
}
